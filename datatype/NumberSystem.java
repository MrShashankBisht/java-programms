class NumberSystem{

        public static void main(String ar[])
          {
            int x=10;    //decimal number system
            int y=010;   //octal number system
            int z=0x10;  //hexadecimal number system
            int w=0b10;  // binary number system
            System.out.println("x is "+x);      
            System.out.println("y="+y);
            System.out.println("z="+z);
            System.out.println("w="+w);
          }

                             }